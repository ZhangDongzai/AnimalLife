# 属性类
class Propertie:
    NAME: str = "propertie"             # 属性名称
    RESTRAINED_PROPERTIES: list = []    # 克制属性
    DISSOLVE_PROPERTIES: list = []      # 融合属性

    # BUFF
    ATTACK: float = 0.0                 # 攻击力
    DEFENSE: float = 0.0                # 防御力
    VITALITY: float = 0.0               # 生命力
